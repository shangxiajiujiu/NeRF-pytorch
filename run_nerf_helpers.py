import torch
import torch.nn.functional as F
from skimage.metrics import structural_similarity as ssim


def compute_ssim(img1, img2):
    # Assume img1 and img2 are numpy arrays with shape (H, W, C)
    return ssim(img1, img2, multichannel=True, data_range=img1.max() - img1.min())


def ssim_loss(pred_img, true_img):
    pred_img_np = pred_img.detach().cpu().numpy()
    true_img_np = true_img.detach().cpu().numpy()
    ssim_value = compute_ssim(pred_img_np, true_img_np)
    return 1 - ssim_value


def combined_loss(pred_img, true_img, alpha=0.8, beta=0.2):
    mse_loss = F.mse_loss(pred_img, true_img)
    ssim_loss_value = ssim_loss(pred_img, true_img)
    return alpha * mse_loss + beta * ssim_loss_value